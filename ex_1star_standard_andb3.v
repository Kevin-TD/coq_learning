Inductive bool : Type :=
  | true 
  | false. 
Definition negb (b:bool) : bool :=
  match b with
  | true => false
  | false => true
  end.
Definition andb (b1:bool) (b2:bool) : bool :=
  match b1 with
  | true => b2
  | false => false
  end.
Definition orb (b1:bool) (b2:bool) : bool :=
  match b1 with
  | true => true
  | false => b2
  end.


Definition andb3 (b1:bool) (b2:bool) (b3:bool) : bool :=
  if negb b1 then false 
  else if negb b2 then false 
  else b3.


